// Accessing Elements of an Array

let car = ["bmw m5","Thar","Fortuner","Audi","Creta"]

console.log("car array",car)

// Any element in the array can be accessed using the index number. 
console.log("my favorite car is",car[0])

// accessing first Element of an array
console.log("first element in array",car[0])

//// accessing last Element of an array
console.log("last element in array",car[car.length - 1])

//  Modifying the Array Elements
console.log("car array Third element before changing is",car[2])
car[2] = "i20"

console.log("changed third element",car[2])

console.log("car after added",car)


